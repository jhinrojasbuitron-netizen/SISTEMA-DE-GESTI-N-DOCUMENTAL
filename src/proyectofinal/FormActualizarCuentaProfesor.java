    private void txtPasswordNuevoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtPasswordNuevoMousePressed
        // Creamos un metodo en el cual se ejecuta cuando presionamos en el cuadro de texto
        // Evaluamos el txt selecionado y ponemos el texto predeterminado si el usuario no pone nada
        if(txtPasswordNuevo.getText().equals("Ingrese su contrasenia nueva...")){
            txtPasswordNuevo.setText("");
        }
        txtPasswordNuevo.setForeground(Color.black);
        // Evaluamos los demas txts y si no hay nada se pone el texto predeterminado
        if(txtUsuarioNuevo.getText().equals("")){
            txtUsuarioNuevo.setText("Ingrese su usuario nuevo...");
            txtUsuarioNuevo.setForeground(Color.gray);
        }
        if(txtRepetirPasswordNuevo.getText().equals("")){
            txtRepetirPasswordNuevo.setText("Repetir su contrasenia nueva...");
            txtRepetirPasswordNuevo.setForeground(Color.gray);
        }
    }//GEN-LAST:event_txtPasswordNuevoMousePressed

