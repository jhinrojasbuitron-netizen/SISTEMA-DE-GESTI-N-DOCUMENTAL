    // Método para validar la contraseńa
    private boolean validarPassword(String password) {
        // Verificar longitud mínima
        if(password.length() < 8) {
            return false;
        }

        // Variables para verificar cada requisito
        boolean tieneMayuscula = false;
        boolean tieneMinuscula = false;
        boolean tieneNumero = false;
        boolean tieneEspecial = false;

        // Caracteres especiales permitidos
        String caracteresEspeciales = "@#$%^&+=!*()_-{}[]|:;'<>,.?/~`";

        // Recorrer cada caracter de la contraseńa
        for(char c : password.toCharArray()) {
            if(Character.isUpperCase(c)) {
                tieneMayuscula = true;
            } else if(Character.isLowerCase(c)) {
                tieneMinuscula = true;
            } else if(Character.isDigit(c)) {
                tieneNumero = true;
            } else if(caracteresEspeciales.indexOf(c) != -1) {
                tieneEspecial = true;
            }
        }

        // Retornar true solo si cumple todos los requisitos
        return tieneMayuscula && tieneMinuscula && tieneNumero && tieneEspecial;
    }
