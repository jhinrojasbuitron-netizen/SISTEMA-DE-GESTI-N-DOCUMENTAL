    private void txtNombreAlumnoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreAlumnoKeyTyped
        char c = evt.getKeyChar();

        // 1. Validar solo letras y espacios (y la tecla de borrar)
        if (!Character.isLetter(c) && (c != ' ') && (c != java.awt.event.KeyEvent.VK_BACK_SPACE)) {
            evt.consume(); // Ignora el caracter
        }

        // 2. Validar longitud máxima
        // (Se resta 1 a la longitud si se está seleccionando texto para reemplazarlo)
        if (txtNombreAlumno.getText().length() >= 50 && txtNombreAlumno.getSelectedText() == null) {
            evt.consume(); // Ignora el caracter si ya se alcanzó el límite
        }
    }//GEN-LAST:event_txtNombreAlumnoKeyTyped

