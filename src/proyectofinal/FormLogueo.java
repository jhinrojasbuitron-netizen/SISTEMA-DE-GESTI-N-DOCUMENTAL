    private void txtUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyTyped
    // Obtiene el caracter que se tecleó
        char c = evt.getKeyChar();

        // Comprueba si el caracter NO es una letra, NO es un dígito,
        // NO es la tecla de "borrar" Y TAMPOCO es un punto.
        if (!Character.isLetterOrDigit(c) && (c != java.awt.event.KeyEvent.VK_BACK_SPACE) && (c != '.')) {
            // Si no cumple ninguna de esas condiciones, "consume" el evento
            evt.consume();
        }
    }//GEN-LAST:event_txtUsuarioKeyTyped

