    private boolean comprobarDni(){
        String dni = txtDniProfesor.getText();

        // Verificar que tenga exactamente 8 dígitos
        if(dni.length() != 8){
            JOptionPane.showMessageDialog(this, 
                "El DNI debe tener exactamente 8 dígitos.\n" +
                "Actualmente tiene: " + dni.length() + " caracteres",
                "DNI inválido",
                JOptionPane.WARNING_MESSAGE);
            txtDniProfesor.requestFocus();
            return false;
        }

        // Verificar que todos sean números
        for(char c : dni.toCharArray()){
            if(!Character.isDigit(c)){
                JOptionPane.showMessageDialog(this, 
                    "El DNI solo debe contener números.\n" +
                    "Se encontró un carácter inválido: '" + c + "'",
                    "DNI inválido",
                    JOptionPane.WARNING_MESSAGE);
                txtDniProfesor.requestFocus();
                return false;
            }
        }

        return true;
    }
