    private void cmbNombreAlumnoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbNombreAlumnoItemStateChanged
        // Creamos un evento en el cual se ejecuta cuando seleccionamos un item del comboBox
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            // Ponemos esta condicional para que no evalue el indice 0
            if(cmbNombreAlumno.getSelectedIndex()!=0){
                // El item selecionado del comboBox se excribe en el txt
                txtNombreABuscarAlumno.setText((String)cmbNombreAlumno.getSelectedItem());
                txtNombreABuscarAlumno.setForeground(Color.black);
                // Removemos los items y buscamos al alumno seleccionado, luego hacemos un for que recorra la listaMatricula el cual 
                // nos retornara en los cursos que ha sido matriculado el alumno
                cmbNombreCurso.removeAllItems();
                cmbNombreCurso.addItem("Elija un curso...");
                Alumno alum = repo.buscarPorNombreAlumno(txtNombreABuscarAlumno.getText());
                for(Matricula x:repo.listarMatricula()){
                    if(x.getCodAlumno()==alum.getCodAlumno()){
                        Curso curso = repo.buscarPorCodigoCurso(x.getCodCurso());
                        cmbNombreCurso.addItem(curso.getNameCurso());
                    }
                }
            }
        }
    }//GEN-LAST:event_cmbNombreAlumnoItemStateChanged

    private void btnBuscarAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarAlumnoMouseClicked
        // Creamos un evento en el cual se ejecuta cuando hacemos click en el boton
        // Ponemos una condicional para evaluar si hay texto en el txtNombreABuscarAlumno
        if(txtNombreABuscarAlumno.getText().equals("")||cmbNombreCurso.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this,"No ha digitado ni un nombre para hacer la busqueda");
            txtNombreABuscarAlumno.setFocusable(true);
            txtCodigoMatricula.setText("Se mostrara el codigo aqui...");
            txtNombreAlumno.setText("Se mostrara el nombre aqui...");
            txtCursoAlumno.setText("Se mostrara el curso aqui...");
            txtTurnoAlumno.setText("Se mostrara el turno de estudio aqui...");
            txtGradoAlumno.setText("Se mostrara el grado escolar aqui...");
            txtNombreProfesor.setText("Se mostrara el nombre aqui...");
        }else{
            // Despues de pasar el filtro buscamos el objeto
            // Utilizamos el nombre del alumno para poder buscar con el metodo buscarPorNombreAlumno
            // Utilizamos el nombre del curso para poder buscar con el metodo buscarPorNombreCurso
            Alumno alum = repo.buscarPorNombreAlumno(txtNombreABuscarAlumno.getText());
            Curso cursos = repo.buscarPorNombreCurso((String)cmbNombreCurso.getSelectedItem());
            Matricula matriculaBuscado = repo.buscarPorCodigoMatricula(alum.getCodAlumno(),cursos.getCodCurso());
            Profesor profe = repo.buscarPorCodigoProfesor(matriculaBuscado.getCodProfesor());
            
            // Despues de obtener el objeto buscado lo asignamos a variables
            int codigo = matriculaBuscado.getCodMatricula();
            String nombre = alum.getNameAlumno();
            String curso = cursos.getNameCurso();
            String turno = matriculaBuscado.getTurnoEstudio();
            String grado = matriculaBuscado.getGradoEscolar();
            String nombrePro = profe.getNameProfesor();

            //Aqui le damos el color negro y colocamos las varibles string en los txts
            txtCodigoMatricula.setDisabledTextColor(Color.BLACK);
            txtNombreAlumno.setDisabledTextColor(Color.BLACK);
            txtCursoAlumno.setDisabledTextColor(Color.BLACK);
            txtTurnoAlumno.setDisabledTextColor(Color.BLACK);
            txtGradoAlumno.setDisabledTextColor(Color.BLACK);
            txtNombreProfesor.setDisabledTextColor(Color.BLACK);

            txtCodigoMatricula.setText(Integer.toString(codigo));
            txtNombreAlumno.setText(nombre);
            txtCursoAlumno.setText(curso);
            txtTurnoAlumno.setText(turno);
            txtGradoAlumno.setText(grado);
            txtNombreProfesor.setText(nombrePro);
        }
    }//GEN-LAST:event_btnBuscarAlumnoMouseClicked

