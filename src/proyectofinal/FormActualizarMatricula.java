    // En esta funcion se utilizara para llenar el comboBox con el nombre o las coincidencias que encuentra con todos los nombres de cada objeto Alumno
    void busarYPonerComboNombreAlumno(){
        cmbNombreAlumno.removeAllItems();
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            if(x.getNameAlumno().toLowerCase().contains(txtNombreABuscarAlumno.getText().toLowerCase())){
                cmbNombreAlumno.addItem(x.getNameAlumno());
            }
        }
    }
    
    //Llenamos el comboBox del Curso, Alumno y CursoProfesor
    private void llenarComboBoxs(){
        cmbCursoNuevoAlumno.addItem("Elija un curso...");
        for(Curso x:repo.listarCurso()){
            if(x.getCodCurso()!=1){
            cmbCursoNuevoAlumno.addItem(x.getNameCurso());                
            }
        }    
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            cmbNombreAlumno.addItem(x.getNameAlumno());
        }
        cmbNombreCurso.addItem("Elija un curso...");
        cmbNombreNuevoProfesor.addItem("Elija un nombre...");
    } 
    
