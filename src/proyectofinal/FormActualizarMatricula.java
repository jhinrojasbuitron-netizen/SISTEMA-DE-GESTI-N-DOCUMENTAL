    private void btnActualizarAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarAlumnoMouseClicked
        // Creamos un evento en el cual se ejecuta cuando hacemos click en el boton
        // Ponemos una condicional para evaluar si hay texto en el txtNombreABuscar
        if(cmbTurnoNuevoAlumno.getSelectedIndex()==0||cmbGradoNuevoAlumno.getSelectedIndex()==0||cmbNombreNuevoProfesor.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this,"Hay un dato o varios en blanco\nPor favor completelos");
            txtNombreAlumno.setFocusable(true);
        }else{
            // Despues de pasar los filtros se actualizaran los datos de la matricula
            // Utilizamos el nombre para poder buscar con el metodo buscarPorNombreAlumno
            Alumno alum = repo.buscarPorNombreAlumno(txtNombreABuscarAlumno.getText());
            int codAlumno = alum.getCodAlumno();
            Profesor profe = repo.buscarPorNombreProfesor((String)cmbNombreNuevoProfesor.getSelectedItem());
            int codProfe = profe.getCodProfesor();   
            // Se crean las variables que extraeran de los txts que tienen los datos nuevos
            String curso = (String)cmbCursoNuevoAlumno.getSelectedItem();
            String turno = cmbTurnoNuevoAlumno.getSelectedItem().toString();
            String grado = cmbGradoNuevoAlumno.getSelectedItem().toString();
            Curso cr = repo.buscarPorNombreCurso(curso);
            
            // Creamos un objeto con los datos nuevo y lo mandamos al metodo actualizarAlumno que tiene como parametro
            // el objeto con los nuevos datos y el codigo del alumno para hacer el cambio respectivo
            Matricula matri = new Matricula(codAlumno,codProfe,cr.getCodCurso(),turno,"",grado);
            repo.actualizarMatricula(matri,Integer.parseInt(txtCodigoMatricula.getText()));
            repo.agregarMatriculaCSV(repo.listarMatricula());
            // Vaciamos los cuadros de texto y ponemos el diseńo predeterminado que escogimos y al final enviamos un mensaje
            vaciarTxt();
            txtDiseno();
            JOptionPane.showMessageDialog(this,"Matricula actualizada correctamente :D");
            txtNombreAlumno.setFocusable(true);
        }        
    }//GEN-LAST:event_btnActualizarAlumnoMouseClicked

