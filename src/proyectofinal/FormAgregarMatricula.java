            txtNombreProfesor.setText((String)cmbNombreProfesor.getSelectedItem());
            txtNombreProfesor.setForeground(Color.black);
            profe = repo.listarProfesor().stream().filter(p->p.getNameProfesor().equals((String)cmbNombreProfesor.getSelectedItem())).collect(Collectors.toList()).get(0);
            txtDniProfesor.setText((String)profe.getDniProfesor());
