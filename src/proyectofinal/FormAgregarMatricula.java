    void llenarComboBoxs(){
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            if(x.getEstadoAlumno()==1){
                cmbNombreAlumno.addItem(x.getNameAlumno());
            }            
        }       
        cmbNombreProfesor.addItem("Elija un nombre...");
        cmbCursoAlumno.addItem("Elija un curso...");
        for(Curso x:repo.listarCurso()){
            if(x.getCodCurso()!=1){
            cmbCursoAlumno.addItem(x.getNameCurso());                
            }
        }         
    }
    
    void busarYPonerComboNombreAlumno(){
        cmbNombreAlumno.removeAllItems();
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            if(x.getNameAlumno().toLowerCase().contains(txtNombreAlumno.getText().toLowerCase())){
                cmbNombreAlumno.addItem(x.getNameAlumno());
            }
        }
    }
       
