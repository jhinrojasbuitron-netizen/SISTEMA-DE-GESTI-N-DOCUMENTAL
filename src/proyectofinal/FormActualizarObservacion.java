    private void btnAgregarObservacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarObservacionMouseClicked
        if(txtObservacionAlumno.getText().equals("")||txtObservacionAlumno.getText().equals("Ingerese la observacion...")){
            JOptionPane.showMessageDialog(this,"No ha digitado ni un nombre para agregar la observacion");
            txtObservacionAlumno.setFocusable(true);

        }
        else{
            Alumno alum = repo.buscarPorNombreAlumno(txtNombreABuscarAlumno.getText());
            Matricula matriculaBuscado = repo.buscarPorCodigoMatricula(alum.getCodAlumno(),logueo.profesor.getCodCurso());

            Matricula matri = new Matricula(matriculaBuscado.getCodAlumno(),matriculaBuscado.getCodProfesor(),matriculaBuscado.getCodCurso(),matriculaBuscado.getTurnoEstudio(),matriculaBuscado.getGradoEscolar(),txtObservacionAlumno.getText());
            repo.agregarObservacion(matri,matriculaBuscado.getCodMatricula());
            repo.agregarMatriculaActuCSV(matri);
            
            txtNombreAlumno.setText("Se mostrara el nombre aqui...");
            txtNombreAlumno.setForeground(Color.gray);
            txtCursoAlumno.setText("Se mostrara el curso aqui...");
            txtCursoAlumno.setForeground(Color.gray);
            txtTurnoAlumno.setText("Se mostrara el turno de estudio aqui...");
            txtTurnoAlumno.setForeground(Color.gray);
            txtGradoAlumno.setText("Se mostrara el grado escolar aqui...");
            txtGradoAlumno.setForeground(Color.gray);
            
            txtDiseno();
            txtObservacionAlumno.setText("Ingerese la observacion...");
            txtObservacionAlumno.setForeground(Color.gray);
            
            JOptionPane.showMessageDialog(this,"Observacion agregada correctamente :D");
            txtNombreABuscarAlumno.setFocusable(true);    
        }
    }//GEN-LAST:event_btnAgregarObservacionMouseClicked

