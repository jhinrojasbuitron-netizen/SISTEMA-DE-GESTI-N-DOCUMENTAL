    private void txtNombreNuevoAlumnoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreNuevoAlumnoKeyTyped
        char c = evt.getKeyChar();
            if (!Character.isLetter(c) && (c != ' ') && (c != java.awt.event.KeyEvent.VK_BACK_SPACE)) {
                evt.consume(); 
            }
            if (txtNombreNuevoAlumno.getText().length() >= 50 && txtNombreNuevoAlumno.getSelectedText() == null) {
                evt.consume();
            }
    }//GEN-LAST:event_txtNombreNuevoAlumnoKeyTyped

