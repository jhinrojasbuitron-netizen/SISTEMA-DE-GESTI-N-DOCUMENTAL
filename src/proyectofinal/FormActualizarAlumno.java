    private void btnBuscarAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarAlumnoMouseClicked
        // Creamos un evento en el cual se ejecuta cuando hacemos click en el boton
        // Ponemos una condicional para evaluar si hay texto en el txtNombreABuscar
        if(txtNombreABuscar.getText().equals("")||txtNombreABuscar.getText().equals("Ingerese el nombre a buscar...")){
            JOptionPane.showMessageDialog(this,"No ha digitado ni un nombre para hacer la busqueda");
            txtNombreAlumno.setFocusable(true);
            txtCodigoAlumno.setText("Se mostrara el codigo aqui...");
            txtNombreAlumno.setText("Se mostrara el nombre aqui...");
            txtApellidoAlumno.setText("Se mostrara el apellido aqui...");
            txtCorreoAlumno.setText("Se mostrara el correo aqui...");
            txtDniAlumno.setText("Se mostrara el dni aqui...");
            txtGeneroAlumno.setText("Se mostrara el curso aqui...");
        }else if(repo.buscarPorNombreAlumno(txtNombreABuscar.getText())==null){
            JOptionPane.showMessageDialog(this,"El nombre digitado no existe");
            txtNombreAlumno.setFocusable(true);
            txtCodigoAlumno.setText("Se mostrara el codigo aqui...");
            txtNombreAlumno.setText("Se mostrara el nombre aqui...");
            txtApellidoAlumno.setText("Se mostrara el apellido aqui...");
            txtCorreoAlumno.setText("Se mostrara el correo aqui...");
            txtDniAlumno.setText("Se mostrara el dni aqui...");
            txtGeneroAlumno.setText("Se mostrara el curso aqui...");
        }else{
            // Despues de pasar el filtro buscamos el objeto
            // Utilizamos el nombre para poder buscar con el metodo buscarPorNombreAlumno
            String nombres = txtNombreABuscar.getText();
            Alumno alumnoBuscado = repo.buscarPorNombreAlumno(nombres);
            
            // Despues de obtener el objeto buscado lo asignamos a variables
            int codigo = alumnoBuscado.getCodAlumno();
            String nombre = alumnoBuscado.getNameAlumno();
            String apellido = alumnoBuscado.getApellidoAlumno();
            String correo = alumnoBuscado.getCorreoAlumno();
            String dni = alumnoBuscado.getDniAlumno();
            String genero = repo.obtenerGenero(alumnoBuscado);

            //Aqui le damos el color negro y colocamos las varibles string en los txts
            txtCodigoAlumno.setDisabledTextColor(Color.BLACK);       
            txtNombreAlumno.setDisabledTextColor(Color.BLACK);
            txtApellidoAlumno.setDisabledTextColor(Color.BLACK);
            txtCorreoAlumno.setDisabledTextColor(Color.BLACK);
            txtDniAlumno.setDisabledTextColor(Color.BLACK);
            txtGeneroAlumno.setDisabledTextColor(Color.BLACK);
            txtCodigoAlumno.setText(Integer.toString(codigo));
            txtNombreAlumno.setText(nombre);
            txtApellidoAlumno.setText(apellido);
            txtCorreoAlumno.setText(correo);
            txtDniAlumno.setText(dni);
            txtGeneroAlumno.setText(genero);               
        }
    }//GEN-LAST:event_btnBuscarAlumnoMouseClicked

