    //Llenamos el comboBox del Genero
    private void llenarCombo() {
        cmbGeneroNuevoAlumno.addItem("Elija una opcion...");
        for(Genero x:repo.listarGenero()){
            cmbGeneroNuevoAlumno.addItem(x.getNameGenero());
        }
    }

    private void busarYPonerComboNombreAlumno(){
        cmbNombreAlumno.removeAllItems();
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            if(x.getNameAlumno().toLowerCase().contains(txtNombreABuscar.getText().toLowerCase())){
                cmbNombreAlumno.addItem(x.getNameAlumno());
            }
        }
    }

    private void llenarComboBoxs(){
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Alumno x:repo.listarAlumno()){
            if(x.getEstadoAlumno()==1){
                cmbNombreAlumno.addItem(x.getNameAlumno());
            }            
        }            
    } 
    
