    private void txtTelefonoNuevoProfesorMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtTelefonoNuevoProfesorMousePressed
        // Creamos un metodo en el cual se ejecuta cuando presionamos en el cuadro de texto
        // Evaluamos el txt selecionado y ponemos el texto predeterminado si el usuario no pone nada
        if(txtTelefonoNuevoProfesor.getText().equals("Ingerese el telefono del profesor...")){
            txtTelefonoNuevoProfesor.setText("");
        }   
        txtTelefonoNuevoProfesor.setForeground(Color.black);
        // Evaluamos los demas txts y si no hay nada se pone el texto predeterminado
        if(txtApellidoNuevoProfesor.getText().equals("")){
            txtApellidoNuevoProfesor.setText("Ingerese su apellido completo...");
            txtApellidoNuevoProfesor.setForeground(Color.gray);
        }
        if(txtNombreNuevoProfesor.getText().equals("")){
            txtNombreNuevoProfesor.setText("Ingerese su nombre completo...");
            txtNombreNuevoProfesor.setForeground(Color.gray);
        }
        if(txtCorreoNuevoProfesor.getText().equals("")){
            txtCorreoNuevoProfesor.setText("Ingerese su correo personal...");
            txtCorreoNuevoProfesor.setForeground(Color.gray);
        }
        if(txtDniNuevoProfesor.getText().equals("")){
            txtDniNuevoProfesor.setText("Ingerese el dni del profesor...");
            txtDniNuevoProfesor.setForeground(Color.gray);
        }
    }//GEN-LAST:event_txtTelefonoNuevoProfesorMousePressed

