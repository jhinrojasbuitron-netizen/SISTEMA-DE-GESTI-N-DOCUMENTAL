    //Llenamos el comboBox del CursoProfesor
    private void llenarCombo() {
        cmbCursoNuevoProfesor.addItem("Elija una opcion...");
        for(int i=0;i<repo.listarCurso().size();i++){
            if(repo.listarCurso().get(i).getCodCurso()!=1){
            cmbCursoNuevoProfesor.addItem(repo.listarCurso().get(i).getNameCurso());                
            }
        }
    }

    private void llenarComboBoxs(){
        cmbNombreAlumno.addItem("Elija un nombre...");
        for(Profesor x:repo.listarProfesor()){
            if(x.getCodProfesor()!=1){
                cmbNombreAlumno.addItem(x.getNameProfesor());
            }
        }           
    }
    
